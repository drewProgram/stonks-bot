import config from './config.js';

import initGame from './game.js';

const { prefix } = config;
function initMessages(client) {
    client.on('message', msg => {
        if (msg.content === `${prefix}play`)
            initGame(msg);
    
        if (msg.content === `${prefix}ping`) {
            msg.reply('pong');
        }
    
        if (msg.content === `${prefix}pong`) {
            msg.reply('Só ping!!!');
        }

        if (msg.content === `${prefix}server`)
            msg.channel.send(`O nome do servidor é: ${msg.guild.name}`);

        if (msg.content === `${prefix}user-info`) 
            msg.channel.send(`Seu nome de usuário é: ${msg.author.username} \nSeu ID: ${msg.author.id}`);
    
        if (msg.content === 'Menina do Stonks')
            msg.reply('STONKS mestre');
    });
}

export default initMessages;
